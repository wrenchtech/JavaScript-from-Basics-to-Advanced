// Números en JS
console.clear()
var num1 = 10 //Entero
var num2 = 10.9 //Decimal
var num3 = 15

console.log(typeof(num1))
console.log(typeof(num2))

//Suma
var sum = num3 + num1 + num2
console.log(sum);

//Resta
var res = num3 - num1
console.log(res);

//Multiplicación
var mul = num3 * num1
console.log(mul);

var mulByString = num3 * "A"
console.log(mulByString);
console.log(typeof(mulByString));

//División
var div = num3 / num1
console.log(div);

var div_entre_0 = num3 / 0
console.log(div_entre_0);
console.log(typeof(div_entre_0));
