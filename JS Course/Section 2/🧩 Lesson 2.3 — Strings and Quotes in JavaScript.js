// Strings in JavaScript
console.clear();

// Strings represent text and are written inside quotes
var myFirstString = "I love JS";
var mySecondString = "We'll learn JS";

// Escape characters (\) allow special characters inside strings
var doubleQuotesString = "This is a \"JavaScript\" string";
var doubleQuotesString2 = 'This is a "JavaScript" string';

// Single quotes can also be escaped when needed
var singleQuoteString = "This is a \'JavaScript\' string";
var singleQuotesString2 = "This is a 'JavaScript' string";

// typeof confirms that these values are strings
console.log(typeof(myFirstString));
console.log(typeof(mySecondString));

// Output examples
console.log(doubleQuotesString);
console.log(doubleQuotesString2);
console.log(singleQuoteString);
console.log(singleQuotesString2);
