//Operaciones

console.clear()

var num1 = 10;
var num2 = 15;
var num3 = 20;

//Suma
console.log(num1 + num2)
console.log(num1 + num2 + num3)

//Resta
console.log(num2 - num1)
console.log(num3 - num2 - num1)

//Multiplicación
console.log(num1 * num2)

//División
console.log(num1/num2)

//Modulo
console.log(num2 % num1) // Te da el residuo entero de una division

//Incremento
console.log(num1++) //Aumenta el valor de una variable por 1
console.log(num1++)
console.log(num1)

console.log(++num1)
console.log(num1)

//Decremento
console.log(num1--) // Reduce el valor de una variable por 1
console.log(num1)

console.log(--num1)
console.log(num1)
