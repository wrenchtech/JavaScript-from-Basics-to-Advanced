// Clears the console before running the program
console.clear();

// Variables store values that can be reused
// Strings represent text and must be written inside quotes
var num1 = "Hello";
var num2 = " World";
var num3 = "!";

// The + operator joins strings into a single value
var sum = num1 + num2 + num3;

// Prints the result to the console
console.log(sum);
